class Solution {
  public:
    int countPairs(vector<int> &arr, int target) {
        int count=0;
        int n=arr.size();
        sort(arr.begin(),arr.end());
        int st=0;
        int end=n-1;
        while(st<end){
            if(arr[st]+arr[end]<target){
                count+=end-st;
                st++;
            }else{
                end--;
            }
            
        }
        return count;
    }
};


